package labs_examples.access_modifiers;

public class ClassOne {

    /* ACCESS MODIFIERS -affect classes, instance variables and methods
        they play a large role in Encapsulation - one of the primary
        tenants of Object-Oriented Programming

        public
            any class, instance variable or method that is marked as "public" can
            be openly accessible from anywhere in the application. it can also be accessed by external
            applications that are using this application.

        private
            only accessible from within the class where the private variable or method is declared.
            we do not have a thing such as a private class.

        protected
            only accessible by classes (and their members) that are declared in the same package
            as the variable or method that is declared "protected". in addition, subclasses whether
            they are in the same package or not, can access protected members.

        default
            same as protected but not accessible by subclasses.


     */
}
