package labs_examples.fundamentals.labs;

/**
 * Fundamentals Exercise 4: My Second Program
 *
 *      Write the necessary code to display, declare and print each of Java's primitive data types.
 *      Please declare a single variable of each type, give it an appropriate value, then print that variable.
 *
 */

public class Exercise_04 {

    public static void main(String[] args) {

        System.out.println("Kat"+ "'"+"s" + " code below:");

        byte a = 12;
        short b = 30000;
        int c = 500000;
        long d = 812234534332L;
        float e = 3.2456f;
        double f = 4.567567567;
        char letter = 'K';
        boolean thisWorks = true;

        System.out.println("Byte a equals: " + a);
        System.out.println("Short b equals: " + b);
        System.out.println("Int c equals: " + c);
        System.out.println("Long d equals: " + d);
        System.out.println("Float e equals: " + e);
        System.out.println("Double f equals: " + f);
        System.out.println("Char equals: " + letter);
        System.out.println("Boolean thisWorks equals: " + thisWorks);

    }

}
